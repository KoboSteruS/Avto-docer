"""
Команда для обновления услуги "Хромирование" с подробной информацией
"""
from django.core.management.base import BaseCommand
from services.models import Service
from loguru import logger


class Command(BaseCommand):
    """
    Команда для обновления услуги "Хромирование" с детальной информацией
    """
    help = 'Обновляет услугу "Хромирование" с подробной информацией'

    def handle(self, *args, **options):
        """
        Обновление услуги "Хромирование"
        """
        try:
            service = Service.objects.get(slug='hrom')
        except Service.DoesNotExist:
            self.stdout.write(
                self.style.ERROR('Услуга "Хромирование" не найдена. Сначала создайте её через create_services.')
            )
            return

        # Краткое описание для карточек
        short_description = (
            'Хромирование деталей, дисков и пластика. Декоративное хромирование для тюнинга автомобиля. '
            'Защита от коррозии и придание блестящего премиального вида.'
        )

        # Основное описание
        description = (
            'Наше автоателье предлагает Вам услуги по хромированию деталей, хромированию дисков, хромирование пластика, '
            'декоративное хромирование, хромирование деталей.\n\n'
            'Хромирование деталей – это насыщение поверхности хромом или осаждение на деталь хрома. Хром – отличный материал для '
            'защиты поверхности от воздействия внешних неприятных факторов. Также хромирование деталей позволяет им приобрести красивый '
            'вид и придать жесткость.\n\n'
            'Данный способ покрытия деталей хромом, активно используется в тюнинге автомобилей. За счет эффективности хрома, который имеет '
            'эстетичный вид, износостойкость и стоек к коррозии.'
        )

        # Дополнительный контент (HTML поддерживается)
        content = (
            '<h2>Хромирование дисков</h2>\n'
            '<p>Так же, очень популярно хромирование дисков. Хромированные диски привлекают внимание окружающих своим блеском. Так же диски '
            'защищены от внешней среды и воздействия агрессивных сред.</p>\n\n'
            '<h2>Хромирование пластика</h2>\n'
            '<p>Хромирование пластика так же популярно. Хромирование пластика позволяет придать металлический вид пластиковым поверхностям и '
            'увеличить их прочность.</p>\n\n'
            '<h2>Декоративное хромирование</h2>\n'
            '<p>Декоративное хромирование применяется для украшения декора автомобиля и придания ему индивидуальности.</p>\n\n'
            '<h2>Методы хромирования</h2>\n'
            '<h3>Гальваническое хромирование</h3>\n'
            '<p>Гальваническое хромирование – более старый метод, нежели химическое хромирование. Этот метод обеспечивает качественное покрытие '
            'и долговечность.</p>\n\n'
            '<h3>Химическое хромирование</h3>\n'
            '<p>Химическое хромирование более экологично и намного дешевле. Этот современный метод позволяет получить качественное покрытие при '
            'меньших затратах.</p>\n\n'
            '<h2>Применение в тюнинге</h2>\n'
            '<p>Хромирование часто используют при общем тюнинге автомобилей. Благодаря своим свойствам, хром позволяет:</p>\n'
            '<ul>\n'
            '<li>придать автомобилю премиальный внешний вид</li>\n'
            '<li>защитить детали от коррозии</li>\n'
            '<li>увеличить износостойкость</li>\n'
            '<li>придать жесткость деталям</li>\n'
            '<li>создать индивидуальный стиль автомобиля</li>\n'
            '</ul>'
        )

        # Список направлений/услуг
        features = (
            'Хромирование деталей автомобиля\n'
            'Хромирование дисков\n'
            'Хромирование пластика\n'
            'Декоративное хромирование\n'
            'Гальваническое хромирование\n'
            'Химическое хромирование\n'
            'Защита от коррозии\n'
            'Защита от внешней среды\n'
            'Придание металлического вида пластику\n'
            'Увеличение прочности деталей\n'
            'Придание жесткости\n'
            'Износостойкое покрытие\n'
            'Эстетичный внешний вид\n'
            'Применение в тюнинге автомобилей\n'
            'Индивидуальный стиль'
        )

        # Обновляем услугу
        service.short_description = short_description
        service.description = description
        service.content = content
        service.features = features
        service.save()

        logger.info(f'Услуга "{service.title}" успешно обновлена')
        self.stdout.write(
            self.style.SUCCESS(f'Услуга "{service.title}" успешно обновлена с подробной информацией')
        )
